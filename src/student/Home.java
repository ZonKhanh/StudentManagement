    private void jButton22ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton22ActionPerformed
        if (!jTextField12.getText().isEmpty() && !jTextField14.getText().isEmpty() && !jTextField17.getText().isEmpty()
                && !jTextScore1.getText().isEmpty() && !jTextScore2.getText().isEmpty() && !jTextScore3.getText().isEmpty()
                && !jTextScore4.getText().isEmpty() && !jTextScore5.getText().isEmpty()) {

            try {
                int sid = Integer.parseInt(jTextField12.getText());
                int semesterNo = Integer.parseInt(jTextField17.getText());

                if (!score.isIdExist(Integer.parseInt(jTextField14.getText()))) {
                    if (!score.isSidSemesterNoExist(sid, semesterNo)) {
                        if (isnumberic(jTextScore1.getText()) && isnumberic(jTextScore2.getText())
                                && isnumberic(jTextScore3.getText()) && isnumberic(jTextScore4.getText())
                                && isnumberic(jTextScore5.getText())) {

                            int id = score.getMax();
                            String course1 = jTextCourse1.getText();
                            String course2 = jTextCourse2.getText();
                            String course3 = jTextCourse3.getText();
                            String course4 = jTextCourse4.getText();
                            String course5 = jTextCourse5.getText();
                            double score1 = Double.parseDouble(jTextScore1.getText());
                            double score2 = Double.parseDouble(jTextScore2.getText());
                            double score3 = Double.parseDouble(jTextScore3.getText());
                            double score4 = Double.parseDouble(jTextScore4.getText());
                            double score5 = Double.parseDouble(jTextScore5.getText());
                            double average = (score1 + score2 + score3 + score4 + score5) / 5;
                            nf.setMaximumFractionDigits(2);

                            score.insert(id, sid, semesterNo, course1, course2, course3, course4, course5,
                                    score1, score2, score3, score4, score5, Double.parseDouble(nf.format(average)));

                            // Thm d liu vo bng
                            DefaultTableModel model = (DefaultTableModel) jTable3.getModel();
                            Object[] row = {id, sid, semesterNo, course1, score1, course2, score2, course3,
                                score3, course4, score4, course5, score5, average};
                            model.addRow(row);
                        } else {
                            JOptionPane.showMessageDialog(this, "Please enter valid numeric scores");
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "Semester " + semesterNo + " score already added");
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Score id already exists");
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Invalid input. Please enter valid numeric values.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please fill in all fields");
        }

    }//GEN-LAST:event_jButton22ActionPerformed

    private boolean isnumberic(String s) {
        try {
            double d = Double.parseDouble(s);
            if (d >= 0.0 && d <= 10.0) {
                return true;
            } else {
                JOptionPane.showMessageDialog(this, "Please enter a valid value, it must be between 0.0 to 10.0");
                return false;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "" + e);

        }
        return false;
    }
