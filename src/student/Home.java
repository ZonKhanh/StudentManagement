    private void jButton20ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton20ActionPerformed
        if (searchField2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Search field must not be empty");
        } else {
            DefaultTableModel originalModel = (DefaultTableModel) jTable3.getModel();

            // To mt bng mi  cha kt qu tm kim
            DefaultTableModel resultModel = new DefaultTableModel(null,
                    new Object[]{"ID", "Student ID", "Semester", "Course 1", "Score1", "Course 2", "Score2", "Course 3",
                        "Score3", "Course 4", "Score4", "Course 5", "Score5", "Average"});

            // Duyt qua cc dng ca bng v thm vo kt qu nu Student ID khp vi t kha
            for (int i = 0; i < originalModel.getRowCount(); i++) {
                Object studentID = originalModel.getValueAt(i, 1); // Ct Student ID  index 1
                if (studentID != null && studentID.toString().toLowerCase().contains(searchField2.getText().toLowerCase())) {
                    // Thm dng tm thy vo bng kt qu
                    resultModel.addRow(originalModel.getDataVector().elementAt(i));
                }
            }

            // Kim tra xem c kt qu no hay khng
            if (resultModel.getRowCount() > 0) {
                // Hin th kt qu trn bng
                jTable3.setModel(resultModel);
            } else {
                // Nu khng c kt qu, hin th thng bo v khi phc bng gc
                JOptionPane.showMessageDialog(this, "Student ID not found: " + searchField2.getText());
                jTable3.setModel(originalModel);
            }
        }
    }//GEN-LAST:event_jButton20ActionPerformed


    private void jButton15ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton15ActionPerformed
        if (jTextField25.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Search field must not be empty");
        } else {
            DefaultTableModel originalModel = (DefaultTableModel) jTable3.getModel();

            // To mt bng mi  cha kt qu tm kim
            DefaultTableModel resultModel = new DefaultTableModel(null,
                    new Object[]{"ID", "Student ID", "Semester", "Course 1", "Score1", "Course 2", "Score2", "Course 3",
                        "Score3", "Course 4", "Score4", "Course 5", "Score5", "Average"});

            // Duyt qua cc dng ca bng v thm vo kt qu nu Student ID khp vi t kha
            for (int i = 0; i < originalModel.getRowCount(); i++) {
                Object studentID = originalModel.getValueAt(i, 1); // Ct Student ID  index 1
                if (studentID != null && studentID.toString().toLowerCase().contains(jTextField25.getText().toLowerCase())) {
                    // Thm dng tm thy vo bng kt qu
                    resultModel.addRow(originalModel.getDataVector().elementAt(i));
                }
            }

            // Kim tra xem c kt qu no hay khng
            if (resultModel.getRowCount() > 0) {
                // Hin th kt qu trn bng
                jTable5.setModel(resultModel);
                DefaultTableModel model = (DefaultTableModel) jTable5.getModel();

                int rowCount = model.getRowCount();
                double totalAverage = 0.0;

                for (int i = 0; i < rowCount; i++) {
                    // Ly gi tr t ct "Average" ti hng th i
                    Object averageValue = model.getValueAt(i, model.findColumn("Average"));

                    // Kim tra xem gi tr c phi l s hay khng
                    if (averageValue instanceof Number) {
                        totalAverage += ((Number) averageValue).doubleValue();
                    } else {
                        // X l trng hp gi tr khng phi l s (c th l null hoc khng hp l)
                        System.out.println("Gi tr khng hp l ti hng " + i);
                    }
                }

// Tnh trung bnh cng
                double cgpa = rowCount > 0 ? totalAverage / rowCount : 0.0;

// nh dng CGPA  ch c 3 ch s thp phn
                DecimalFormat df = new DecimalFormat("#.###");
                String formattedCGPA = df.format(cgpa);

// Hin th kt qu
                jLabel27.setText("CGPA: " + formattedCGPA);

// Hin th kt qu
            } else {
                // Nu khng c kt qu, hin th thng bo v khi phc bng gc
                JOptionPane.showMessageDialog(this, "Student ID not found: " + jTextField25.getText());
                jTable5.setModel(originalModel);
            }
        }

    }//GEN-LAST:event_jButton15ActionPerformed

