    public boolean isEmtyStudent() {

        if (jTextField2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student name is missing");
            return false;
        }

        if (jDateChooser1.getDate() != null) {
    // Ly ngy sinh t jDateChooser1
    Date birthDate = jDateChooser1.getDate();

    // To mt i tng Calendar v thit lp ngy hin ti
    Calendar currentDate = Calendar.getInstance();

    // Tr 6 nm t ngy hin ti
    currentDate.add(Calendar.YEAR, -6);

    // So snh ngy sinh sau khi tr i 6 nm vi ngy hin ti
    if (birthDate.after(currentDate.getTime())) {
        JOptionPane.showMessageDialog(this, "The minimum age is 18");
        return false;
    }
} else {
    // X l trng hp ngy sinh khng c chn
    JOptionPane.showMessageDialog(this, "Date of birth is missing");
    return false;
}

        if (jTextField3.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student email is missing");
            return false;
        }

        if (!jTextField3.getText().matches("^.+@.+\\..+$")) {
            JOptionPane.showMessageDialog(this, "Invalid email address");
            return false;
        }

        if (jTextField4.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student phone number is missing");
            return false;
        }

        if (jTextField4.getText().length() > 10) {
            JOptionPane.showMessageDialog(this, "Invalid phone number");
            return false;
        }

        if (jTextField5.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student father's name is missing");
            return false;
        }

        if (jTextField6.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Student mother's name is missing");
            return false;
        }

        if (jTextField7.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Address Line 1 is missing");
            return false;
        }

        if (jTextField8.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Address Line 2 is missing");
            return false;
        }

        if (imagePath == null) {
            JOptionPane.showMessageDialog(this, "Please select your image");
            return false;
        }
        return true;
    }
    
    public boolean check(){
        String newEmail = jTextField3.getText();
        String newPhone = jTextField4.getText();
        String oldEmail = model.getValueAt(rowIndex,4).toString();
        String oldPhone = model.getValueAt(rowIndex,5).toString();
        if(oldEmail.equals(newEmail) && oldPhone.equals(newPhone)){
            return false;
        }else{
            if(!newEmail.equals(oldEmail)){
                boolean x = student.isEmailExist(newEmail);
                if(x){
                    JOptionPane.showMessageDialog(this, "This email address already exist");
                    return x;
                }
            }
            if(!newPhone.equals(oldPhone)){
                boolean x = student.isPhoneExist(newPhone);
                if(x){
                    JOptionPane.showMessageDialog(this, "This phone number already exist");
                    return x;
                }
            }
        }
        return false;
    }
