    private void jButton23ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton23ActionPerformed
        int id = Integer.parseInt(jTextField14.getText());
        if (score.isIdExist(id)) {
            if (isnumberic(jTextScore1.getText()) && isnumberic(jTextScore2.getText())
                    && isnumberic(jTextScore3.getText()) && isnumberic(jTextScore4.getText())
                    && isnumberic(jTextScore5.getText())) {
                // Ly d liu mi t cc trng nhp liu
                int sid = Integer.parseInt(jTextField12.getText());
                int semesterNo = Integer.parseInt(jTextField17.getText());
                String course1 = jTextCourse1.getText();
                String course2 = jTextCourse2.getText();
                String course3 = jTextCourse3.getText();
                String course4 = jTextCourse4.getText();
                String course5 = jTextCourse5.getText();
                double score1 = Double.parseDouble(jTextScore1.getText());
                double score2 = Double.parseDouble(jTextScore2.getText());
                double score3 = Double.parseDouble(jTextScore3.getText());
                double score4 = Double.parseDouble(jTextScore4.getText());
                double score5 = Double.parseDouble(jTextScore5.getText());
                double average = (score1 + score2 + score3 + score4 + score5) / 5;
                nf.setMaximumFractionDigits(2);

                score.update(id, score1, score2, score3, score4, score5, average);

                // Cp nht d liu trong JTable
                DefaultTableModel model = (DefaultTableModel) jTable3.getModel();
                int rowCount = model.getRowCount();
                for (int i = 0; i < rowCount; i++) {
                    if ((int) model.getValueAt(i, 0) == id) {
                        model.setValueAt(id, i, 0);
                        model.setValueAt(sid, i, 1);
                        model.setValueAt(semesterNo, i, 2);
                        model.setValueAt(course1, i, 3);
                        model.setValueAt(score1, i, 4);
                        model.setValueAt(course2, i, 5);
                        model.setValueAt(score2, i, 6);
                        model.setValueAt(course3, i, 7);
                        model.setValueAt(score3, i, 8);
                        model.setValueAt(course4, i, 9);
                        model.setValueAt(score4, i, 10);
                        model.setValueAt(course5, i, 11);
                        model.setValueAt(score5, i, 12);
                        model.setValueAt(average, i, 13);
                        break;
                    }
                }
            }

        } else {
            JOptionPane.showMessageDialog(this, "Score ID doesn't exist");
        }
    }//GEN-LAST:event_jButton23ActionPerformed

